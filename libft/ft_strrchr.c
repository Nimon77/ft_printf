char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*here;

	i = -1;
	here = 0;
	while (s[++i] != '\0')
		if (s[i] == c)
			here = (char *)&s[i];
	if (c == '\0')
		return ((char *)&s[i]);
	return (here);
}
