#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	size;
	char	*copy;

	size = ft_strlen(s1) + 1;
	if ((copy = malloc(sizeof(*s1) * size)) == NULL)
		return (NULL);
	ft_strlcpy(copy, s1, size);
	return (copy);
}
